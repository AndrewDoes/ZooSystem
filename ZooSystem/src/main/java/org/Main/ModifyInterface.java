package org.Main;

/**
 * This interface defines methods to add, modify, and remove elements.
 * It is implemented by classes that need to support these operations.
 */
public interface ModifyInterface {

    /**
     * Adds an element.
     */
    void add();
    /**
     * Modifies an element.
     */
    void modify();
    /**
     * Removes an element.
     */
    void remove();

}
